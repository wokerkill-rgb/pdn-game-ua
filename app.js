async function refreshAdminStatus() {
  try {
    const response = await fetch("api/admin/status", { cache: "no-store" });
    const data = await response.json();
    adminAuthenticated = Boolean(data.authenticated);
    adminName = data.adminName || "";
    if (!data.configured) $("#adminHint").textContent = t("adminNotConfigured");
    return data;
  } catch {
    adminAuthenticated = false;
    adminName = "";
    return { configured: false, authenticated: false };
  }
}

async function submitAdminLogin(name, password) {
  try {
    const response = await secureFetch("api/admin/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ name, password })
    });
    const data = await response.json();
    if (!response.ok || !data.ok) {
      if (response.status === 429) $("#adminHint").textContent = t("adminLocked");
      else if (response.status === 503) $("#adminHint").textContent = t("adminNotConfigured");
      else $("#adminHint").textContent = t("wrongPassword");
      return false;
    }

    adminAuthenticated = true;
    adminName = data.adminName || name;
    $("#adminLogin").hidden = true;
    $("#adminConsole").hidden = false;
    renderAdminServers();
    addAudit(`${adminName}: ${t("adminUnlocked")}`);
    return true;
  } catch {
    $("#adminHint").textContent = t("wrongPassword");
    return false;
  }
}

async function requestServerRestart(server, button) {
  if (!server) return;
  button.disabled = true;
  try {
    const response = await secureFetch(`api/admin/restart/${encodeURIComponent(server.id)}`, { method: "POST" });
    const data = await response.json();
    if (!response.ok || !data.ok) {
      addAudit(response.status === 401 ? t("adminRestartDenied") : (data.error || t("wrongPassword")));
      if (response.status === 401) {
        adminAuthenticated = false;
        $("#adminLogin").hidden = false;
        $("#adminConsole").hidden = true;
      }
      return;
    }
    addAudit(`${t("restartQueued")}: ${data.serverName || server.name}`);
  } catch {
    addAudit(t("adminRestartDenied"));
  } finally {
    setTimeout(() => {
      button.disabled = false;
    }, 1600);
  }
}

