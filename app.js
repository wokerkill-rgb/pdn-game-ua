function applyVoiceInputState(serverId = selectedServerId) {
  if (!localVoiceStream || activeVoiceServerId !== serverId) return;
  const isMuted = mutedServers.has(serverId);
  localVoiceStream.getAudioTracks().forEach((track) => {
    track.enabled = !isMuted;
  });
  if (isMuted) $("#voiceMeter").style.width = "0%";
}

function applyMicInputGain() {
  if (!voiceGainNode) return;
  const gain = Math.max(0, Math.min(2, micInputGain / 100));
  voiceGainNode.gain.setTargetAtTime(gain, voiceAudioContext?.currentTime || 0, 0.01);
}

function applyVoiceOutputState(serverId = selectedServerId) {
  const isDeafened = deafenedServers.has(serverId);
  const volume = Math.max(0, Math.min(1, voiceOutputVolume / 100));
  document.querySelectorAll("[data-voice-output]").forEach((audio) => {
    audio.muted = isDeafened || volume === 0;
    audio.volume = volume;
  });
}

async function startVoiceTest(serverId) {
  if (!window.isSecureContext) {
    $("#voiceStatus").textContent = t("micUnsupported");
    setMicDiagnostic(t("micInsecure"));
    setMicHelp(t("micHelpUnsupported"));
    return false;
  }

  const mediaDevices = getMediaDevices();
  if (!mediaDevices?.getUserMedia && !getBrowserNavigator()?.getUserMedia) {
    $("#voiceStatus").textContent = t("micUnsupported");
    setMicDiagnostic(t("micUnsupported"));
    setMicHelp(t("micHelpUnsupported"));
    return false;
  }

  stopVoiceTest();
  $("#voiceStatus").textContent = t("micChecking");
  setMicDiagnostic(t("micPermissionPrompt"));
  setMicHelp(t("micHelpPrompt"));

  try {
    localVoiceStream = await requestMicrophoneStream();
    activeVoiceServerId = serverId;
    voiceJoinedServers.add(serverId);
    const track = localVoiceStream.getAudioTracks()[0];
    applyVoiceInputState(serverId);
    applyVoiceOutputState(serverId);
    setMicDiagnostic(`${t("micReady")}: ${track?.label || "default input"}`);
    setMicHelp(t("micHelpReady"));
    updateMicDiagnostics();
    startVoiceMeter(localVoiceStream, serverId);
    return true;
  } catch (error) {
    $("#voiceStatus").textContent = t("micDenied");
    setMicDiagnostic(formatMicError(error));
    setMicHelp(formatMicHelp(error));
    $("#voiceMeter").style.width = "0%";
    return false;
  }
}

function stopVoiceTest(serverId = activeVoiceServerId) {
  if (serverId) voiceJoinedServers.delete(serverId);
  if (voiceMeterFrame) cancelAnimationFrame(voiceMeterFrame);
  voiceMeterFrame = null;
  voiceAnalyser = null;
  voiceGainNode = null;

  if (localVoiceStream) {
    localVoiceStream.getTracks().forEach((track) => track.stop());
    localVoiceStream = null;
  }

  if (voiceAudioContext) {
    voiceAudioContext.close();
    voiceAudioContext = null;
  }

  $("#voiceMeter").style.width = "0%";
}

function startVoiceMeter(stream, serverId) {
  const AudioContextClass = window.AudioContext || window.webkitAudioContext;
  if (!AudioContextClass) {
    $("#voiceStatus").textContent = t("micListening");
    $("#voiceMeter").style.width = "100%";
    return;
  }

  voiceAudioContext = new AudioContextClass();
  const source = voiceAudioContext.createMediaStreamSource(stream);
  voiceGainNode = voiceAudioContext.createGain();
  voiceAnalyser = voiceAudioContext.createAnalyser();
  voiceAnalyser.fftSize = 512;
  applyMicInputGain();
  source.connect(voiceGainNode);
  voiceGainNode.connect(voiceAnalyser);

  const samples = new Uint8Array(voiceAnalyser.frequencyBinCount);
  const tick = () => {
    if (!voiceAnalyser || activeVoiceServerId !== serverId || !voiceJoinedServers.has(serverId)) return;
    if (mutedServers.has(serverId)) {
      $("#voiceStatus").textContent = t("micOff");
      $("#voiceMeter").style.width = "0%";
      voiceMeterFrame = requestAnimationFrame(tick);
      return;
    }
    if (micInputGain <= 0) {
      $("#voiceStatus").textContent = t("micListening");
      $("#voiceMeter").style.width = "0%";
      voiceMeterFrame = requestAnimationFrame(tick);
      return;
    }
    voiceAnalyser.getByteFrequencyData(samples);
    const average = samples.reduce((sum, value) => sum + value, 0) / samples.length;
    const level = Math.min(100, Math.round(average * 1.6));
    $("#voiceStatus").textContent = t("micListening");
    $("#voiceMeter").style.width = `${level}%`;
    voiceMeterFrame = requestAnimationFrame(tick);
  };
  tick();
}

