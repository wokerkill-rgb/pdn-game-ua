async function refreshSecurityToken() {
  const response = await fetch("api/security/csrf", { cache: "no-store" });
  if (!response.ok) throw new Error("Security token unavailable");
  const data = await response.json();
  csrfToken = data.csrfToken || "";
  return csrfToken;
}

async function secureFetch(url, options = {}, retry = true) {
  const method = (options.method || "GET").toUpperCase();
  const headers = new Headers(options.headers || {});
  if (method !== "GET") {
    if (!csrfToken) await refreshSecurityToken();
    headers.set("X-PDN-CSRF", csrfToken);
  }

  const response = await fetch(url, {
    ...options,
    headers
  });

  if (response.status === 403 && retry && method !== "GET") {
    await refreshSecurityToken();
    return secureFetch(url, options, false);
  }

  return response;
}

function renderUpdateStatus() {
  const button = $("#updateButton");
  const status = $("#updateStatus");
  if (!button || !status) return;

  button.disabled = updateState.phase === "checking" || updateState.phase === "installing" || updateState.phase === "restarting";
  button.classList.toggle("is-ready", updateState.phase === "ready");

  const label = button.querySelector("strong");
  if (label) label.textContent = updateState.phase === "ready" ? t("updateInstall") : t("updateButton");

  if (updateState.phase === "checking") status.textContent = t("updateChecking");
  else if (updateState.phase === "installing") status.textContent = t("updateInstalling");
  else if (updateState.phase === "restarting") status.textContent = t("updateRestarting");
  else if (updateState.phase === "ready") status.textContent = `${t("updateAvailable")} ${updateState.latestVersion}`;
  else if (updateState.phase === "current") status.textContent = `${t("updateCurrent")} ${updateState.currentVersion}`;
  else if (updateState.phase === "failed") status.textContent = t("updateFailed");
  else status.textContent = t("updateIdle");
}

async function checkForUpdates() {
  updateState = {
    ...updateState,
    phase: "checking"
  };
  renderUpdateStatus();

  try {
    const response = await fetch("api/app/update/check", { cache: "no-store" });
    const data = await response.json().catch(() => ({}));
    if (!response.ok || !data.ok) throw new Error(data.error || "Update check failed");

    updateState = {
      phase: data.updateAvailable ? "ready" : "current",
      updateAvailable: Boolean(data.updateAvailable),
      latestVersion: data.latestVersion || data.currentVersion || "",
      currentVersion: data.currentVersion || ""
    };
    renderUpdateStatus();
    return data;
  } catch (error) {
    console.warn("PDN Game UA update check failed.", error);
    updateState = {
      ...updateState,
      phase: "failed",
      updateAvailable: false
    };
    renderUpdateStatus();
    return null;
  }
}

async function installAvailableUpdate() {
  if (!updateState.updateAvailable) {
    const data = await checkForUpdates();
    if (!data?.updateAvailable) return;
  }

  updateState = {
    ...updateState,
    phase: "installing"
  };
  renderUpdateStatus();

  try {
    const response = await secureFetch("api/app/update/install", { method: "POST" });
    const data = await response.json().catch(() => ({}));
    if (!response.ok || !data.ok) throw new Error(data.error || "Update install failed");

    updateState = {
      ...updateState,
      phase: data.restarting ? "restarting" : "current",
      updateAvailable: false,
      latestVersion: data.latestVersion || updateState.latestVersion,
      currentVersion: data.latestVersion || updateState.currentVersion
    };
    renderUpdateStatus();
  } catch (error) {
    console.warn("PDN Game UA update install failed.", error);
    updateState = {
      ...updateState,
      phase: "failed"
    };
    renderUpdateStatus();
  }
}

async function handleUpdateButton() {
  if (updateState.phase === "ready") {
    await installAvailableUpdate();
    return;
  }
  await checkForUpdates();
}

