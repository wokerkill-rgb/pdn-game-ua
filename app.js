function getBrowserNavigator() {
  if (typeof navigator !== "undefined") return navigator;
  if (typeof window !== "undefined" && window.navigator) return window.navigator;
  return null;
}

function getMediaDevices() {
  return getBrowserNavigator()?.mediaDevices || null;
}

async function requestMicrophoneStream() {
  const mediaDevices = getMediaDevices();
  if (mediaDevices?.getUserMedia) {
    try {
      return await mediaDevices.getUserMedia({
        audio: {
          echoCancellation: true,
          noiseSuppression: true,
          autoGainControl: true
        }
      });
    } catch (error) {
      if (error?.name !== "OverconstrainedError" && error?.name !== "ConstraintNotSatisfiedError") {
        throw error;
      }
      return mediaDevices.getUserMedia({ audio: true });
    }
  }

  const nav = getBrowserNavigator();
  const legacyGetUserMedia = nav?.getUserMedia || nav?.webkitGetUserMedia || nav?.mozGetUserMedia;
  if (!legacyGetUserMedia) throw new Error("getUserMedia unavailable");

  return new Promise((resolve, reject) => {
    legacyGetUserMedia.call(nav, { audio: true }, resolve, reject);
  });
}

async function updateMicDiagnostics() {
  if (!$("#voiceDiagnostic")) return;

  if (!window.isSecureContext) {
    setMicDiagnostic(t("micInsecure"));
    setMicHelp(t("micHelpUnsupported"));
    return;
  }

  const mediaDevices = getMediaDevices();
  if (!mediaDevices?.enumerateDevices) {
    setMicDiagnostic(t("micUnsupported"));
    setMicHelp(t("micHelpUnsupported"));
    return;
  }

  try {
    const permissionState = await getMicPermissionState();
    const devices = await mediaDevices.enumerateDevices();
    const microphones = devices.filter((device) => device.kind === "audioinput");
    if (!microphones.length) {
      setMicDiagnostic(t("micNoDevices"));
      setMicHelp(t("micHelpNoDevices"));
      return;
    }
    const names = microphones
      .map((device, index) => device.label || `Mic ${index + 1}`)
      .slice(0, 2)
      .join(", ");
    const permission = permissionState ? ` · ${formatPermissionState(permissionState)}` : "";
    setMicDiagnostic(`${t("micDevices")}: ${microphones.length}${names ? ` · ${names}` : ""}${permission}`);
    if (permissionState === "denied") setMicHelp(t("micHelpDenied"));
    else if (permissionState === "prompt") setMicHelp(t("micHelpDefault"));
  } catch (error) {
    setMicDiagnostic(formatMicError(error));
    setMicHelp(formatMicHelp(error));
  }
}

async function getMicPermissionState() {
  const nav = getBrowserNavigator();
  if (!nav?.permissions?.query) return "";
  try {
    const status = await nav.permissions.query({ name: "microphone" });
    return status?.state || "";
  } catch {
    return "";
  }
}

function formatPermissionState(state) {
  if (state === "granted") return t("micPermissionGranted");
  if (state === "prompt") return t("micPermissionPromptState");
  if (state === "denied") return t("micPermissionDeniedState");
  return state;
}

function formatMicError(error) {
  const name = error?.name || "Error";
  if (name === "NotAllowedError" || name === "SecurityError") return `${t("micDenied")} · ${name}`;
  if (name === "NotFoundError" || name === "DevicesNotFoundError") return `${t("micNoDevices")} · ${name}`;
  if (name === "NotReadableError" || name === "TrackStartError") return `${t("micDenied")} · ${name}`;
  return `${t("micDenied")} · ${name}`;
}

function formatMicHelp(error) {
  const name = error?.name || "Error";
  if (name === "NotAllowedError" || name === "SecurityError") return t("micHelpDenied");
  if (name === "NotFoundError" || name === "DevicesNotFoundError") return t("micHelpNoDevices");
  if (name === "NotReadableError" || name === "TrackStartError") return t("micHelpBusy");
  if (name === "OverconstrainedError" || name === "ConstraintNotSatisfiedError") return t("micHelpNoDevices");
  return t("micHelpUnsupported");
}

