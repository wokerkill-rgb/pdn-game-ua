function pollSteamAuth(loginId, attempt) {
  if (steamAuthPollTimer) clearTimeout(steamAuthPollTimer);
  if (!loginId || attempt > 90) {
    $("#profileHint").textContent = t("steamConsentFailed");
    setSteamConsentBusy(false);
    return;
  }

  steamAuthPollTimer = setTimeout(async () => {
    try {
      const response = await fetch(`api/auth/steam/status?login=${encodeURIComponent(loginId)}`, { cache: "no-store" });
      const data = await response.json();
      if (response.status === 202 || data.status === "pending") {
        pollSteamAuth(loginId, attempt + 1);
        return;
      }
      if (data.status === "complete" && data.profile) {
        applyProfile(data.profile);
        setSteamConsentBusy(false);
        return;
      }
      throw new Error(data.error || data.status || "Steam auth failed");
    } catch (error) {
      console.warn("Steam auth polling failed.", error);
      $("#profileHint").textContent = t("steamConsentFailed");
      setSteamConsentBusy(false);
    }
  }, attempt === 0 ? 1200 : 1600);
}

